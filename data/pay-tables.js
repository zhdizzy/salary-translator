/**
 * Get base pay for a grade and years of service.
 * Finds the highest YOS bracket <= the given YOS.
 */
export function getBasePay(grade, yearsOfService) {
    const table = BASE_PAY[grade];
    if (!table) return 0;

    // Get the available brackets for this grade, sorted
    const brackets = Object.keys(table).map(Number).sort((a, b) => a - b);

    // Find the highest bracket <= yearsOfService
    let bracket = brackets[0];
    for (const b of brackets) {
        if (yearsOfService >= b) bracket = b;
        else break;
    }
    return table[bracket] || 0;
}
